def spin(isotope: str) -> float:
    """
    Return the spin quantum number of the requested isotope.

    Parameters
    ----------
    isotope : str
        Nucleus symbol (e.g. `'1H'`) used to select the spin quantum number.

    Returns
    -------
    spin : float
        Spin quantum number.
    """
    return ISOTOPES[isotope][SPIN_INDEX]


def gamma(isotope: str, unit: Literal["Hz", "rad/s"]) -> float:
    """
    Return the gyromagnetic ratio of the requested isotope.

    Parameters
    ----------
    isotope : str
        Nucleus symbol (e.g. `'1H'`) used to select the gyromagnetic ratio.
    unit : {'Hz', 'rad/s'}
        Specifies in which unit the gyromagnetic ratio is returned.

    Returns
    -------
    gamma : float
        Gyromagnetic ratio in the requested units.

    Raises
    ------
    ValueError
        Raised if `unit` is neither `'Hz'` nor `'rad/s'`.
    """

    # Convert the tabulated value from MHz/T to Hz/T.
    gamma_value = ISOTOPES[isotope][GAMMA_INDEX] * 1e6

    # Return the requested unit representation.
    if unit == "Hz":
        return gamma_value

    if unit == "rad/s":
        return gamma_value * 2 * np.pi

    raise ValueError(f"Incorrect units: {unit}")


def quadrupole_moment(isotope: str) -> float:
    """
    Return the quadrupole moment in units of m^2.

    Parameters
    ----------
    isotope : str
        Nucleus symbol (e.g. `'1H'`) used to select the quadrupole moment.

    Returns
    -------
    quadrupole_moment : float
        Quadrupole moment in units of m^2.
    """

    # Convert the tabulated value from barns to square metres.
    return ISOTOPES[isotope][QUADRUPOLE_INDEX] * 1e-28


def atomic_mass(isotope: str) -> float:
    """
    Return the atomic mass of the requested isotope.

    Parameters
    ----------
    isotope : str
        Nucleus symbol (e.g. `'1H'`) used to select the atomic mass.

    Returns
    -------
    atomic_mass : float
        Atomic mass in atomic mass units (u).
    """
    return ISOTOPES[isotope][ATOMIC_MASS_INDEX]


def natural_abundance(isotope: str) -> float:
    """
    Return the natural abundance of the requested isotope in per cent.

    Parameters
    ----------
    isotope : str
        Nucleus symbol (e.g. `'1H'`) used to select the natural abundance.

    Returns
    -------
    natural_abundance : float
        Natural abundance in per cent.
    """
    return ISOTOPES[isotope][NATURAL_ABUNDANCE_INDEX]